    static getText(id) {
        const text = this.texts[id];
        const userLang = this.getUserLanguage();
		const text_found = text ? text[userLang] : `${id}_${userLang}`;
        return text_found.replace('<br>', '\n');
    }

    static getUserLanguage() {
		let lang = _getenv('user.lang', 'en');
		if (lang == "zh") {
			lang = "zhs";
		}
        return lang;
    }
